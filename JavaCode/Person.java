package JavaCode;

public class Person {
    private String name;
    private int age;
    private String rollno;

    public String getName(){
        return name;
    }

    public int getAge(){
        return age;
    }

    public String getRollno(){
        return rollno;
    }

    public void setName(String name){
        this.name = name;
    }

    public void setAge(int age){
        this.age = age;
    }

    public void setRollno(String rollno){
        this.rollno = rollno;
    }   
}
